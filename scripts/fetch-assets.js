/* =========================================================
   CoreStorm Global — scripts/fetch-assets.js
   Localises every remote asset so the site is fully
   self-hosted: no Unsplash hot-linking, no Google Fonts
   requests at runtime. Run once (or whenever imagery
   changes), commit the results in assets/.

     node scripts/fetch-assets.js

   Downloads:
     assets/img/*.jpg          — stock photos, resized per use
     assets/fonts/*.woff2      — Playfair Display 600 (+italic),
                                 Inter 400/500/600 (latin subset)
     assets/fonts/fonts.css    — generated @font-face rules

   Requires Node 18+ (global fetch) and "sharp".
   ========================================================= */

