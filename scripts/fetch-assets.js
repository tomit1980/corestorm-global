/* Google Fonts css2 specs to localise (latin subset only). */
const FONTS = [
  {
    family: "Playfair Display",
    css2: "family=Playfair+Display:ital,wght@0,600;1,600",
  },
  {
    family: "Inter",
    css2: "family=Inter:wght@400;500;600",
  },
];

const log = (...a) => console.log(...a);

async function fetchBuffer(url) {
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return Buffer.from(await res.arrayBuffer());
}

async function fetchImages() {
  log(`\n🖼  Downloading ${IMAGES.length} image variants…`);
  // Cache originals per photo id so each id downloads once.
  const originals = new Map();
  for (const { id, out, w, q } of IMAGES) {
    if (!originals.has(id)) {
      const url = `https://images.unsplash.com/photo-${id}?auto=format&fit=crop&w=1900&q=85`;
      originals.set(id, await fetchBuffer(url));
    }
    const buf = await sharp(originals.get(id))
      .resize({ width: w, withoutEnlargement: true })
      .jpeg({ quality: q, mozjpeg: true })
      .toBuffer();
    fs.writeFileSync(path.join(IMG_DIR, out), buf);
    log(`   ✓ ${out.padEnd(24)} ${w}w  ${(buf.length / 1024) | 0}KB`);
  }
}

async function fetchFonts() {
  log(`\n🔤 Downloading fonts (latin subset)…`);
  let cssOut =
    "/* Generated by scripts/fetch-assets.js — self-hosted fonts */\n";

  for (const font of FONTS) {
    const url = `https://fonts.googleapis.com/css2?${font.css2}&display=swap`;
    const res = await fetch(url, { headers: { "User-Agent": UA } });
    if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
    const css = await res.text();

    // Keep only the /* latin */ blocks.
    const blocks = css.split("/*").map((b) => "/*" + b);
    const latin = blocks.filter((b) => b.trim().startsWith("/* latin */"));

    for (let block of latin) {
      const m = block.match(/url\((https:\/\/[^)]+\.woff2)\)/);
      if (!m) continue;
      const remote = m[1];
      const style = /font-style:\s*italic/.test(block) ? "italic" : "normal";
      const weight = (block.match(/font-weight:\s*(\d+)/) || [])[1] || "400";
      const fname = `${font.family.toLowerCase().replace(/\s+/g, "-")}-${weight}-${style}.woff2`;

      const buf = await fetchBuffer(remote);
      fs.writeFileSync(path.join(FONT_DIR, fname), buf);
      log(`   ✓ ${fname.padEnd(36)} ${(buf.length / 1024) | 0}KB`);

      block = block.replace(remote, fname); // relative to fonts.css
      cssOut += block.trim() + "\n";
    }
  }
  fs.writeFileSync(path.join(FONT_DIR, "fonts.css"), cssOut);
  log(`   ✓ fonts.css written`);
}

(async () => {
  try {
    fs.mkdirSync(IMG_DIR, { recursive: true });
    fs.mkdirSync(FONT_DIR, { recursive: true });
    await fetchImages();
    await fetchFonts();
    log(`\n✅ All assets localised under assets/ — the site now runs with zero external requests.`);
  } catch (err) {
    console.error("\n💥 fetch-assets failed:", err.message);
    process.exit(1);
  }
})();
